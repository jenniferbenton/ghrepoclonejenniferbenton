my_string = "Hello World"

# Indexing examples
first_char = my_string[0]  # "H"
last_char = my_string[-1]  # "d"

# Slicing examples
hello = my_string[0:5]  # "Hello"
world = my_string[6:]   # "World"

print(first_char)
print(last_char)
print(hello)
print(world)

