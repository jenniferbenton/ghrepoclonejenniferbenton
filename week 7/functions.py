def average_age(ages_list:list):
    """

    :param ages_list: Should be a list of ages
    :return: average age in float

    """
    sum_ages = sum(ages_list) #this will give sum of ages
    num_ages = len(ages_list) # get no. of ages in list
    avg_list = sum_ages / num_ages # this is average age
    return avg_list

